def _webhook_post_sync(webhook_url: str, payload: dict, files: dict | None = None,
                       timeout: float = 10.0) -> bool:
    """POST to a Discord webhook synchronously. Returns True on 2xx."""
    try:
        if files:
            data = {"payload_json": json.dumps(payload)}
            r = requests.post(webhook_url, data=data, files=files, timeout=timeout)
        else:
            r = requests.post(webhook_url, json=payload, timeout=timeout)
        return 200 <= r.status_code < 300
    except Exception as e:
        print(f"[webhook] post failed: {e}")
        return False


def _encode_png_bytes(screenshot) -> bytes | None:
    """Accept numpy array / PIL Image / None. Return PNG bytes or None."""
    if screenshot is None:
        return None
    try:
        img = screenshot
        if isinstance(img, np.ndarray):
            img = Image.fromarray(img)
        if not isinstance(img, Image.Image):
            return None
        buf = io.BytesIO()
        img.save(buf, format="PNG")
        return buf.getvalue()
    except Exception:
        return None


_PYLA_CARD_PALETTE = (
    '#F8B733', '#2D7DD2', '#E85D75', '#8E6D3C', '#D13B3B', '#8FB339',
    '#C36CD6', '#5FAD56', '#F7A928', '#D97441', '#6EBAA7', '#4E4B87',
    '#5B8DEF', '#B45EE8', '#64A33B', '#F2A33A', '#E25858', '#E56BAF',
)


def _color_for_key(key: str) -> str:
    """Deterministic accent colour for a brawler key — mirrors colorForKey in store.jsx."""
    h = 0
    for ch in key or "":
        h = (h * 31 + ord(ch)) & 0xFFFF
    if not _PYLA_CARD_PALETTE:
        return "#F8B733"
    return _PYLA_CARD_PALETTE[h % len(_PYLA_CARD_PALETTE)]


def _format_duration(seconds: float) -> str:
    try:
        seconds = max(0, int(seconds))
    except (TypeError, ValueError):
        return "—"
    h, rem = divmod(seconds, 3600)
    m, _ = divmod(rem, 60)
    if h > 0:
        return f"{h}h {m:02d}m"
    return f"{m}m"


def _build_milestone_card_payload(brawler: str, summary: dict) -> dict:
    """Translate the bot_runner summary into the CardVariantA payload."""
    curve = summary.get("trophy_curve") or []
    curve = [int(v) for v in curve if v is not None]
    start_val = int(summary.get("session_start_trophies")
                    if summary.get("session_start_trophies") is not None
                    else (curve[0] if curve else (summary.get("trophies") or 0)))
    cur_val = int(summary.get("trophies") if summary.get("trophies") is not None
                  else (curve[-1] if curve else start_val))
    goal_type = summary.get("goal_type") or "trophies"
    target = int(summary.get("goal_target") or max(cur_val + 1, start_val + 1))

    started_at = summary.get("started_at") or 0
    duration_s = max(0, time.time() - float(started_at)) if started_at else 0
    duration = _format_duration(duration_s)

    games = int(summary.get("session_matches") or 0)
    wins = int(summary.get("session_victories") or 0)
    losses = int(summary.get("session_defeats") or 0)
    wr_raw = summary.get("session_winrate")
    if wr_raw is None and games > 0:
        wr_raw = wins / games * 100
    win_rate = int(round(float(wr_raw or 0)))

    brawler_key = (summary.get("brawler_key") or brawler or "").lower()
    color = summary.get("brawler_color") or _color_for_key(brawler_key)

    return {
        "brawler": {"name": str(brawler or "—"), "color": color},
        "mode":    {"name": str(summary.get("mode_name") or ""), "color": "#7C5CFF"},
        "goal":    {"type": goal_type, "current": cur_val,
                    "target": target, "start": start_val},
        "stats":   {
            "games": games,
            "wins": wins,
            "losses": losses,
            "winRate": win_rate,
            "netTrophies": int(summary.get("session_trophy_delta") or 0),
            "duration": duration,
            "winStreak": int(summary.get("win_streak") or 0),
        },
        "curve": curve,
    }


def notify_user(message_type: str, screenshot=None, subject: str | None = None,
                live_summary: dict | None = None) -> bool:
    """Synchronous Discord notification. Safe to call from any thread."""
    try:
        cfg = load_toml_as_dict("cfg/general_config.toml")
    except Exception:
        return False

    webhook_url = (cfg.get("personal_webhook") or "").strip()
    if not webhook_url:
        return False

    if message_type == "error" and not _config_bool(cfg.get("discord_notify_on_error"), False):
        return False

    discord_id = str(cfg.get("discord_id") or "").strip()
    ping = f"<@{discord_id}>" if discord_id else None
    summary = live_summary or {}

    if message_type == "milestone_reached":
        brawler = subject or summary.get("brawler") or "—"
        label = summary.get("milestone_label") or ""
        embed = {
            "title": "Milestone Reached",
            "description": f"{brawler} reached {label}." if label else f"{brawler} hit a milestone.",
            "color": 0x5865F2,
        }
        card_png: bytes | None = None
        try:
            from backend.notify_render import render_milestone_card_a
            card_png = render_milestone_card_a(_build_milestone_card_payload(brawler, summary))
        except Exception as e:
            print(f"[webhook] milestone card render failed: {e}")
        if card_png:
            embed["image"] = {"url": "attachment://session.png"}
            payload = {"username": "Pyla notifier", "embeds": [embed]}
            if ping:
                payload["content"] = ping
            return _webhook_post_sync(
                webhook_url, payload,
                files={"file": ("session.png", card_png, "image/png")},
            )
        trophies = summary.get("trophies")
        delta = summary.get("session_trophy_delta", 0) or 0
        matches = int(summary.get("session_matches") or 0)
        wins = int(summary.get("session_victories") or 0)
        losses = int(summary.get("session_defeats") or 0)
        draws = int(summary.get("session_draws") or 0)
        wr = summary.get("session_winrate") or 0.0
        streak = summary.get("win_streak", 0) or 0
        trophy_str = f"{trophies} ({'+' if delta >= 0 else ''}{delta} session)" if trophies is not None else f"{'+' if delta >= 0 else ''}{delta}"
        embed["fields"] = [
            {"name": "Brawler",    "value": str(brawler),  "inline": True},
            {"name": "Trophies",   "value": trophy_str,    "inline": True},
            {"name": "Session",    "value": f"{wins}W / {losses}L / {draws}D\n{matches} matches ({wr:.1f}% WR)", "inline": True},
            {"name": "Win streak", "value": str(streak),   "inline": True},
            {"name": "Milestone",  "value": label or "—",  "inline": True},
        ]
    elif message_type == "error":
        err = str(summary.get("error") or "unknown")
        embed = {
            "title": "PylaAI — Error",
            "description": err[:1900],
            "color": 0xE74C3C,
        }
    elif message_type == "completed":
        embed = {
            "title": "PylaAI — Goal complete",
            "description": "Pyla has completed all its targets.",
            "color": 0x34D399,
        }
    elif message_type == "bot_is_stuck":
        embed = {
            "title": "PylaAI — Bot stuck",
            "description": "Bot stalled; attempting to restart Brawl Stars.",
            "color": 0xF59E0B,
        }
    else:
        embed = {
            "title": f"PylaAI — {message_type}",
            "description": str(summary)[:1900] if summary else "",
            "color": 0x9AA0AC,
        }

    png = _encode_png_bytes(screenshot)
    if png is not None:
        embed["image"] = {"url": "attachment://screenshot.png"}

    payload = {"username": "Pyla notifier", "embeds": [embed]}
    if ping:
        payload["content"] = ping

    files = {"file": ("screenshot.png", png, "image/png")} if png else None
    return _webhook_post_sync(webhook_url, payload, files=files)
