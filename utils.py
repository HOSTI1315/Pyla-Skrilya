cached_toml = {}

# ---------------------------------------------------------------------------
# Per-instance config dir support
#
# Multi-emulator runs spawn one Python process per emulator, each with its own
# ``instances/N/cfg/`` directory. ``set_config_dir()`` is called by main.py
# very early (before importing modules that read configs at module-load time),
# so ``load_toml_as_dict("cfg/...")`` transparently resolves into the active
# instance's config dir for that subprocess. The web-server process keeps the
# default ``CONFIG_DIR = "cfg"`` and never calls set_config_dir.
# ---------------------------------------------------------------------------
CONFIG_DIR = "cfg"


def set_config_dir(path):
    """Switch which directory ``cfg/...`` paths resolve to.

    Call this before importing modules that load configs at module-load time
    (lobby_automation, play, state_finder, …). Clears the TOML cache so any
    already-loaded configs are re-read from the new location.
    """
    global CONFIG_DIR
    CONFIG_DIR = str(path).replace("\\", "/").rstrip("/")
    cached_toml.clear()


def resolve_cfg_path(file_path):
    """Map a ``cfg/...``-prefixed path into the active CONFIG_DIR.

    Accepts both ``cfg/x.toml`` and ``./cfg/x.toml``. Anything that doesn't
    start with ``cfg/`` is returned unchanged so non-cfg paths (logs,
    screenshots, …) behave the same.
    """
    if file_path is None:
        return file_path
    normalized = str(file_path).replace("\\", "/")
    if normalized.startswith("./"):
        normalized = normalized[2:]
    if normalized.startswith("cfg/"):
        return os.path.join(CONFIG_DIR, normalized[4:]).replace("\\", "/")
    return file_path


def load_toml_as_dict(file_path):
    resolved = resolve_cfg_path(file_path)
    if resolved not in cached_toml:
        if os.path.exists(resolved):
            with open(resolved, 'r', encoding='utf-8-sig') as f:
                cached_toml[resolved] = toml.load(f)
        else:
            cached_toml[resolved] = {}
    return cached_toml[resolved]

def clear_toml_cache(file_path=None):
    if file_path is None:
        cached_toml.clear()
    else:
        cached_toml.pop(resolve_cfg_path(file_path), None)

def save_dict_as_toml(data, file_path):
    resolved = resolve_cfg_path(file_path)
    parent = os.path.dirname(resolved)
    if parent:
        os.makedirs(parent, exist_ok=True)
    with open(resolved, 'w', encoding='utf-8') as f:
        toml.dump(data, f)
    cached_toml[resolved] = data


# NOTE: kept as a module-level string for backward compat (older code imports
# this directly), but it's a *template* — every read/write site MUST resolve
# it through ``resolve_cfg_path`` (or call ``match_log_path()``) so that
# per-instance bot subprocesses write into ``instances/<id>/cfg/`` and not the
# global ``./cfg/`` dir. The previous code used the literal string and
# silently lumped every instance's match data into one shared file.
MATCH_LOG_PATH = "./cfg/match_log.jsonl"


def match_log_path():
    """Return the active match-log path, honoring the per-instance CONFIG_DIR."""
    return resolve_cfg_path(MATCH_LOG_PATH)


def bootstrap_stat_files(cfg_root="cfg"):
    """Create empty default stat files so the web UI doesn't show 'no data'
    on a fresh install or per-instance dir that hasn't seen a match yet.

    Real bot writes (TrophyObserver, sessions, etc.) APPEND to these files,
    so seeding empty content here is non-destructive: existing files are
    left untouched.

    Files seeded:
      * cfg/match_log.jsonl     -> empty, ready to append
      * cfg/match_history.toml  -> {} so /api/stats can read without trying
                                   to parse a missing file
      * cfg/brawler_stats.toml  -> {} same
      * cfg/sessions.jsonl      -> empty
      * cfg/adaptive_state.json -> {} so AdaptiveBrain has a place to land
    """
    if not os.path.isdir(cfg_root):
        return
    seeds = {
        os.path.join(cfg_root, "match_log.jsonl"): "",
        os.path.join(cfg_root, "match_history.toml"): "",
        os.path.join(cfg_root, "brawler_stats.toml"): "",
        os.path.join(cfg_root, "sessions.jsonl"): "",
        os.path.join(cfg_root, "adaptive_state.json"): "{}",
    }
    for path, default_content in seeds.items():
        if not os.path.exists(path):
            try:
                with open(path, "w", encoding="utf-8") as f:
                    f.write(default_content)
            except OSError as exc:
                print(f"bootstrap_stat_files: could not seed {path}: {exc}")


def read_match_log(path=None, limit=None, brawler=None, gamemode=None):
    """Return match entries newest-first, with optional filtering and limit.

    ``path=None`` resolves to ``match_log_path()`` so callers in a per-instance
    bot subprocess get that instance's log automatically. Pass an explicit
    path to read a specific file (e.g. when aggregating across instances).
    """
    if path is None:
        path = match_log_path()
    if not os.path.exists(path):
        return []
    entries = []
    try:
        with open(path, "r", encoding="utf-8") as f:
            for raw in f:
                raw = raw.strip()
                if not raw:
                    continue
                try:
                    obj = json.loads(raw)
                except (ValueError, TypeError):
                    continue
                if brawler and obj.get("brawler") != brawler:
                    continue
                if gamemode and obj.get("gamemode") != gamemode:
                    continue
                entries.append(obj)
    except OSError:
        return []

    entries.reverse()
    if limit is not None and limit >= 0:
        entries = entries[:limit]
    return entries


