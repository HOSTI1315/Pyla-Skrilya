def count_hsv_pixels(cv_image, low_hsv, high_hsv):
    hsv_image = cv2.cvtColor(cv_image, cv2.COLOR_RGB2HSV)
    mask = cv2.inRange(hsv_image, low_hsv, high_hsv)
    return cv2.countNonZero(mask)

def save_brawler_data(data):
    """
    Save the given data to a json file. As a list of dictionaries.

    When running under a per-instance config dir (multi-emulator mode), the
    file lives next to that instance's state so two instances don't race on
    the repo-root copy. The single-instance default keeps the legacy path.
    """
    if CONFIG_DIR and CONFIG_DIR != "cfg":
        # CONFIG_DIR looks like "instances/N/cfg" — sibling state/ dir.
        instance_root = os.path.dirname(CONFIG_DIR.rstrip("/"))
        target_dir = os.path.join(instance_root, "state")
        os.makedirs(target_dir, exist_ok=True)
        target = os.path.join(target_dir, "latest_brawler_data.json")
    else:
        target = "latest_brawler_data.json"
    with open(target, 'w') as f:
        json.dump(data, f, indent=4)
    # Also write a heartbeat snapshot so backend.instance_manager can show
    # current_brawler / brawlers_left in the UI without parsing logs.
    if CONFIG_DIR and CONFIG_DIR != "cfg":
        try:
            head = data[0] if isinstance(data, list) and data else {}
            beat = {
                "ts": time.time(),
                "current_brawler": head.get("brawler") if isinstance(head, dict) else None,
                "brawlers_left": max(0, len(data) - 1) if isinstance(data, list) else 0,
            }
            beat_target = os.path.join(target_dir, "heartbeat.json")
            tmp = beat_target + ".tmp"
            with open(tmp, "w", encoding="utf-8") as f:
                json.dump(beat, f)
            os.replace(tmp, beat_target)
        except Exception:
            pass


