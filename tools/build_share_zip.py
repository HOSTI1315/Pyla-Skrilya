"""Build a clean zip of the project for sharing with a friend.

Excludes the bundled Python runtime (``_internal/`` ~932 MB), build artefacts,
local logs, and personal data. Scrubs API tokens / Discord webhooks / dev
credentials from cfg files so nothing private leaks through. Emits a README
explaining how to install on the friend's machine.

Usage:
    python tools/build_share_zip.py [output_zip_path]
"""

from __future__ import annotations

import os
import sys
import zipfile
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
DEFAULT_OUT = ROOT.parent / "PylaAI-test-share.zip"

# ------------------------------------------------------------------
# What to skip
# ------------------------------------------------------------------
EXCLUDE_DIR_NAMES = {
    "_internal",                # 932 MB PyInstaller bundle for setup.exe
    "__pycache__",
    "build",
    "pylaai_op_xxz.egg-info",
    "logs",                     # local log history
    "instances",                # local multi-emulator state
    "debug_frames",
    ".git",
    ".vscode",
    ".idea",
    "fork", "FORK",             # reference fork, not needed by friend
    "runs",
    "tools/runs",
    ".pytest_cache",
    "node_modules",             # tooling-only deps for local audit scripts
    "Desing Promt",             # designer's prototype workspace, prod uses web_ui/
    # Local test fixtures: personal account screenshots + auto-annotated
    # debug frames. They're useful for our regression dev work but not for
    # a friend's install; they also leak account UI hints.
    "TestScreenshot",
    "LobbyBrawlerTets",
    "_annotated",
}

EXCLUDE_FILE_RELPATHS = {
    "latest_brawler_data.json",
    "cfg/match_log.jsonl",      # match history, can be MB
    "cfg/sessions.jsonl",
    "cfg/adaptive_state.json",
    "cfg/login.toml",           # personal license-style key
    "cfg/match_history.toml",
    "codes.txt",                # unrelated personal note
    "test.txt",
    "setup.exe",                # needs _internal which we excluded
    ".gitignore",
}

EXCLUDE_FILE_SUFFIXES = (
    ".pyc", ".pyo", ".pyd",
)

# ------------------------------------------------------------------
# What to scrub before zipping
# ------------------------------------------------------------------
SCRUBBED_FILES = {
    "cfg/brawl_stars_api.toml": '''api_token = ""
player_tag = "#YOURTAG"
timeout_seconds = 15
auto_refresh_token = false
developer_email = ""
developer_password = ""
public_ip_service = "https://api.ipify.org"
key_name_prefix = "PylaAI Auto"
key_description = "Auto-generated by PylaAI for the current public IP."
delete_old_auto_tokens = true
delete_all_tokens = false
last_public_ip = ""
''',
    "cfg/webhook_config.toml": '''webhook_url = ""
discord_id = ""
username = "PylaAI"
send_match_summary = false
include_screenshot = true
ping_when_stuck = false
ping_when_target_is_reached = false
ping_every_x_match = 0
ping_every_x_minutes = 0
''',
}


def scrub_general_config(text: str) -> str:
    """Strip the personal webhook / player tag / public IP from general_config.toml."""
    out = []
    for line in text.splitlines():
        stripped = line.lstrip()
        if stripped.startswith("personal_webhook"):
            out.append('personal_webhook = ""')
        elif stripped.startswith("discord_id"):
            out.append('discord_id = ""')
        elif stripped.startswith("player_tag"):
            out.append('player_tag = ""')
        elif stripped.startswith("last_public_ip"):
            out.append('last_public_ip = ""')
        else:
            out.append(line)
    return "\n".join(out) + ("\n" if not text.endswith("\n") else "")


