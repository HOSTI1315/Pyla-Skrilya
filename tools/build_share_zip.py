# ------------------------------------------------------------------
# Build
# ------------------------------------------------------------------
def should_skip_dir(rel_parts: tuple[str, ...]) -> bool:
    return any(part in EXCLUDE_DIR_NAMES for part in rel_parts)


def should_skip_file(rel_path: str) -> bool:
    if rel_path.replace("\\", "/") in EXCLUDE_FILE_RELPATHS:
        return True
    if rel_path.endswith(EXCLUDE_FILE_SUFFIXES):
        return True
    return False


def main() -> None:
    out_path = Path(sys.argv[1]) if len(sys.argv) > 1 else DEFAULT_OUT
    out_path = out_path.resolve()
    print(f"[build-share] writing {out_path}")
    if out_path.exists():
        out_path.unlink()

    file_count = 0
    total_bytes = 0
    with zipfile.ZipFile(out_path, "w", compression=zipfile.ZIP_DEFLATED, compresslevel=6) as zf:
        for dirpath, dirnames, filenames in os.walk(ROOT):
            # Prune directories in-place so os.walk doesn't recurse into them.
            dirnames[:] = [d for d in dirnames if d not in EXCLUDE_DIR_NAMES]
            rel_dir = Path(dirpath).resolve().relative_to(ROOT)
            rel_parts = rel_dir.parts if str(rel_dir) != "." else ()
            if should_skip_dir(rel_parts):
                continue

            for fname in filenames:
                rel_path = (rel_dir / fname).as_posix() if rel_parts else fname
                if should_skip_file(rel_path):
                    continue

                src = Path(dirpath) / fname

                # Scrubbed files: write sanitized content instead.
                if rel_path in SCRUBBED_FILES:
                    zf.writestr(f"PylaAI/{rel_path}", SCRUBBED_FILES[rel_path])
                    file_count += 1
                    total_bytes += len(SCRUBBED_FILES[rel_path])
                    print(f"  scrub  {rel_path}")
                    continue

                # Scrub general_config.toml in-place: keep most fields,
                # blank out the personal/sensitive ones.
                if rel_path == "cfg/general_config.toml":
                    try:
                        text = src.read_text(encoding="utf-8-sig")
                    except Exception:
                        text = src.read_text(encoding="utf-8", errors="replace")
                    cleaned = scrub_general_config(text)
                    zf.writestr(f"PylaAI/{rel_path}", cleaned)
                    file_count += 1
                    total_bytes += len(cleaned)
                    print(f"  scrub  {rel_path}")
                    continue

                try:
                    zf.write(src, f"PylaAI/{rel_path}")
                    file_count += 1
                    total_bytes += src.stat().st_size
                except Exception as exc:
                    print(f"  SKIP   {rel_path} ({exc})")

        # Friend-facing README at the root of the zip.
        zf.writestr("PylaAI/README_TEST.txt", README_TEST)
        file_count += 1
        total_bytes += len(README_TEST)

    final_size = out_path.stat().st_size
    print(f"[build-share] done: {file_count} files, raw {total_bytes/1024/1024:.1f} MB, "
          f"compressed {final_size/1024/1024:.1f} MB")
    print(f"[build-share] output: {out_path}")


if __name__ == "__main__":
    main()
